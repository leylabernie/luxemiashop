#!/usr/bin/env node
/**
 * Generate src/lib/goneRoutes.ts from dist/dead-product-handles.json.
 *
 * This script runs BEFORE vite build so that middleware.ts can import the
 * generated GONE_PRODUCT_HANDLES Set at bundle time. The source JSON is
 * produced by scripts/generate-sitemap.cjs when it HEAD-checks every Shopify
 * product URL against the live site.
 *
 * On the first build (or when the JSON doesn't exist), an empty Set is written
 * and the build continues normally. Subsequent builds will pick up the dead
 * handles discovered in the previous build's sitemap pass.
 *
 * Run: node scripts/generate-gone-routes.cjs
 * Automatically run during: npm run build (first step)
 */

const fs = require('fs');
const path = require('path');

const JSON_PATH = path.resolve(__dirname, '../dist/dead-product-handles.json');
const TS_PATH = path.resolve(__dirname, '../src/lib/goneRoutes.ts');

function main() {
  let handles = [];
  if (fs.existsSync(JSON_PATH)) {
    try {
      const raw = JSON.parse(fs.readFileSync(JSON_PATH, 'utf8'));
      if (Array.isArray(raw)) {
        handles = raw.filter(h => typeof h === 'string' && h.length > 0);
      }
    } catch (err) {
      console.warn(`[gone-routes] Could not parse ${JSON_PATH}: ${err.message}`);
    }
  } else {
    console.log('[gone-routes] No dead-product-handles.json found — writing empty set.');
    console.log('[gone-routes] Run `npm run build` again after the first deploy to populate it.');
  }

  // De-duplicate + sort for stable diffs
  const unique = Array.from(new Set(handles)).sort();

  const ts = `// AUTO-GENERATED by scripts/generate-gone-routes.cjs — do not edit manually.
// Regenerated on each build via: node scripts/generate-gone-routes.cjs
//
// Contains Shopify product handles that are KNOWN to be dead (removed from Shopify
// or returning 404/410/500 on the storefront). These get a hard HTTP 410 Gone
// response in middleware.ts so Google drops them from the index faster than a
// 404 would.
//
// Source: dist/dead-product-handles.json (produced by scripts/generate-sitemap.cjs
// after HEAD-checking every Shopify product URL against the live site).
//
// Last generated: ${new Date().toISOString()}
// Handle count: ${unique.length}

export const GONE_PRODUCT_HANDLES: Set<string> = new Set<string>(${JSON.stringify(unique)});
`;

  fs.writeFileSync(TS_PATH, ts, 'utf8');
  console.log(`[gone-routes] Wrote ${TS_PATH} (${unique.length} dead handles)`);
}

main();
